# --- Global Variables ---
BACKUP_ROOT="/opt/rdp_remediation_backups"
TIMESTAMP=$(date +"%Y%m%d_%H%M%S")
SESSION_DIR="$BACKUP_ROOT/SESSION_$TIMESTAMP"
AUDIT_LOG="$SESSION_DIR/audit.log"
export DEBIAN_FRONTEND=noninteractive

# Ensure the script is run as root
if [[ $EUID -ne 0 ]]; then
   echo -e "\e[31m[!] Error: This script requires root privileges. Please run with sudo or as root.\e[0m"
   exit 1
fi

# --- Helper Functions ---

log_audit() {
    local msg="$1"
    echo "[$(date +'%Y-%m-%d %H:%M:%S')] $msg" >> "$AUDIT_LOG"
}

backup_file() {
    local file_path="$1"
    if [[ -f "$file_path" ]]; then
        local rel_path="${file_path#/}"
        mkdir -p "$SESSION_DIR/files/$(dirname "$rel_path")"
        cp -a "$file_path" "$SESSION_DIR/files/$rel_path"
        log_audit "BACKUP: $file_path"
    fi
}

track_package() {
    log_audit "PACKAGE: $1 (Action: $2)"
}

create_recovery_archive() {
    echo ""
    echo "[*] Finalizing backup and generating recovery archive..."
    
    # Generate a standalone restore script inside the backup directory
    cat << 'EOF' > "$SESSION_DIR/restore.sh"
#!/bin/bash
# Standalone Restore Script generated by RDP Remediation Toolkit
if [[ $EUID -ne 0 ]]; then echo "Run as root."; exit 1; fi
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" &> /dev/null && pwd)"
if [[ -d "$SCRIPT_DIR/files" ]]; then
    echo "Restoring files from backup..."
    cp -av "$SCRIPT_DIR/files/"* /
    echo "Files restored successfully."
else
    echo "Error: Backup payload missing."
fi
EOF
    chmod +x "$SESSION_DIR/restore.sh"
    
    # Create Tarball
    cd "$BACKUP_ROOT" || exit
    tar -czf "RDP_BACKUP_$TIMESTAMP.tar.gz" "SESSION_$TIMESTAMP"
    rm -rf "SESSION_$TIMESTAMP"
    echo -e "\e[32m[ PASS ] Recovery archive saved to: $BACKUP_ROOT/RDP_BACKUP_$TIMESTAMP.tar.gz\e[0m"
}

# --- Module: System Report ---

run_system_report() {
    clear
    echo "==========================================================="
    echo " RDP System Diagnostic Report"
    echo "==========================================================="
    
    echo -e "\n\e[36m[1] OS Information\e[0m"
    lsb_release -a 2>/dev/null | grep -E "Description|Release" || grep PRETTY_NAME /etc/os-release
    
    echo -e "\n\e[36m[2] Installed Desktop Environments\e[0m"
    dpkg -l | grep -E "ubuntu-desktop|xfce4 |gdm3|lightdm" | awk '{print $2, $3}' || echo "None found."

    echo -e "\n\e[36m[3] RDP Service Status\e[0m"
    if dpkg -l | grep -q "gnome-remote-desktop"; then
        echo "- gnome-remote-desktop: Installed"
        systemctl is-active --quiet gnome-remote-desktop.service && echo "  -> Service: ACTIVE" || echo "  -> Service: INACTIVE"
    else
        echo "- gnome-remote-desktop: Not Installed"
    fi
    if dpkg -l | grep -q "xrdp"; then
        echo "- xrdp: Installed"
        systemctl is-active --quiet xrdp && echo "  -> Service: ACTIVE" || echo "  -> Service: INACTIVE"
    else
        echo "- xrdp: Not Installed"
    fi

    echo -e "\n\e[36m[4] Network Listening Ports (3389)\e[0m"
    if ss -tulpn | grep -q ":3389\b"; then
        echo -e "\e[32m- Port 3389 is actively listening.\e[0m"
        ss -tulpn | grep ":3389\b" | awk '{print $1, $5, $7}'
    else
        echo -e "\e[31m- Port 3389 is NOT listening.\e[0m"
    fi

    echo -e "\n\e[36m[5] Known Issue Analysis\e[0m"
    ISSUE_FOUND=false
    
    # Check X11 locks
    if ls /tmp/.X*-lock 1> /dev/null 2>&1; then
        echo -e "\e[31m[!] Ghost X11 locks detected in /tmp. These can cause RDP black screens.\e[0m"
        ISSUE_FOUND=true
    fi
    
    # Check ssl-cert group for XRDP
    if dpkg -l | grep -q "xrdp"; then
        if ! grep -q "ssl-cert:.*xrdp" /etc/group; then
            echo -e "\e[31m[!] xrdp user is NOT in the ssl-cert group. TLS Handshakes will fail.\e[0m"
            ISSUE_FOUND=true
        fi
    fi

    # Check GDM3 Auto-Login for GRD User Mode
    if dpkg -l | grep -q "gnome-remote-desktop"; then
        if ! grep -q "AutomaticLoginEnable=True" /etc/gdm3/custom.conf 2>/dev/null; then
            echo -e "\e[33m[i] GDM3 Auto-Login is disabled. GRD 'User Mode' will require physical login first.\e[0m"
        fi
    fi

    if [ "$ISSUE_FOUND" = false ]; then
        echo -e "\e[32m- No obvious misconfigurations detected.\e[0m"
    fi
    
    echo "==========================================================="
    read -p "Press Enter to return to main menu..."
}

# --- Module: Restore System ---

run_restore() {
    clear
    echo "==========================================================="
    echo " Restore System from Backup"
    echo "==========================================================="
    
    if [[ ! -d "$BACKUP_ROOT" ]]; then
        echo "[!] No backup directory found at $BACKUP_ROOT"
        sleep 2
        return
    fi

    echo "Available Recovery Archives:"
    ARCHIVES=($(ls -1 "$BACKUP_ROOT"/*.tar.gz 2>/dev/null))
    
    if [[ ${#ARCHIVES[@]} -eq 0 ]]; then
        echo "[!] No backup archives found."
        sleep 2
        return
    fi

    for i in "${!ARCHIVES[@]}"; do
        echo "  $((i+1))) $(basename "${ARCHIVES[$i]}")"
    done
    echo "  0) Cancel"
    
    read -p "Select an archive to restore [0-$(( ${#ARCHIVES[@]} ))]: " ARCHIVE_CHOICE
    
    if [[ "$ARCHIVE_CHOICE" -gt 0 ]] && [[ "$ARCHIVE_CHOICE" -le "${#ARCHIVES[@]}" ]]; then
        SELECTED_ARCHIVE="${ARCHIVES[$((ARCHIVE_CHOICE-1))]}"
        echo ""
        echo -e "\e[31m[WARNING] This will overwrite current system files with the backup.\e[0m"
        read -p "Are you sure you want to restore $(basename "$SELECTED_ARCHIVE")? [y/N]: " CONFIRM_RESTORE
        if [[ "$CONFIRM_RESTORE" == "y" || "$CONFIRM_RESTORE" == "Y" ]]; then
            TEMP_EXTRACT="/tmp/rdp_restore_temp_$$"
            mkdir -p "$TEMP_EXTRACT"
            tar -xzf "$SELECTED_ARCHIVE" -C "$TEMP_EXTRACT"
            
            # Find the unzipped session folder
            SESSION_FOLDER=$(ls -1 "$TEMP_EXTRACT")
            if [[ -x "$TEMP_EXTRACT/$SESSION_FOLDER/restore.sh" ]]; then
                bash "$TEMP_EXTRACT/$SESSION_FOLDER/restore.sh"
            else
                echo "[!] restore.sh not found or not executable in archive!"
            fi
            
            rm -rf "$TEMP_EXTRACT"
            echo "Restore complete! A reboot is highly recommended."
            read -p "Press Enter to return to menu..."
        fi
    fi
}

# --- Module: Install & Repair ---

run_install_repair() {
    clear
    echo "==========================================================="
    echo " RDP Install & Repair Mode"
    echo "==========================================================="
    
    # Initialize Backup Session
    mkdir -p "$SESSION_DIR/files"
    log_audit "SESSION STARTED"

    # Prompts
    read -p "Perform extensive system, package, and DE repair? [Y/n] (Default: Y): " INPUT_REPAIR
    REPAIR=${INPUT_REPAIR:-Y}
    [[ "$REPAIR" == "y" || "$REPAIR" == "Y" ]] && REPAIR="true" || REPAIR="false"

    echo -e "\nSelect the RDP Service to install:"
    echo "  1) gnome-remote-desktop (Default - Uses Built-in GNOME engine)"
    echo "  2) xrdp (Traditional protocol wrapper)"
    read -p "Enter choice [1-2]: " INPUT_SERVICE
    [[ "$INPUT_SERVICE" == "2" ]] && RDP_SERVICE="xrdp" || RDP_SERVICE="gnome-remote-desktop"

    if [[ "$RDP_SERVICE" == "gnome-remote-desktop" ]]; then
        echo -e "\nSelect Gnome-Remote-Desktop Mode:"
        echo "  1) System Mode (Headless, independent of user login - Default)"
        echo "  2) User Mode (Requires Auto-Login to be enabled)"
        read -p "Enter choice [1-2]: " INPUT_MODE
        [[ "$INPUT_MODE" == "2" ]] && GNOME_MODE="User" || GNOME_MODE="System"
    fi

    echo ""
    while true; do
        read -p "Enter the target Username for RDP access: " USERNAME
        [[ -n "$USERNAME" ]] && break
        echo "[!] Username cannot be empty."
    done

    # Password with confirmation
    echo ""
    while true; do
        read -sp "Enter the Password for RDP access: " PASSWORD
        echo ""
        read -sp "Confirm the Password: " PASSWORD_CONFIRM
        echo ""
        if [[ "$PASSWORD" == "$PASSWORD_CONFIRM" && -n "$PASSWORD" ]]; then
            break
        else
            echo -e "\e[31m[!] Passwords do not match or are empty. Please try again.\e[0m"
        fi
    done

    echo -e "\nSelect Desktop Manager:"
    echo "  1) Gnome (Default)"
    echo "  2) XFCE4 (Recommended for XRDP due to lower overhead)"
    read -p "Enter choice [1-2]: " INPUT_DM
    [[ "$INPUT_DM" == "2" ]] && DESKTOP_MANAGER="XFCE4" || DESKTOP_MANAGER="Gnome"

    echo -e "\n[i] Starting execution in 3 seconds..."
    sleep 3

    # User Creation
    if ! id "$USERNAME" &>/dev/null; then
        echo "[*] Creating user '$USERNAME'..."
        useradd -m -s /bin/bash "$USERNAME"
        echo "$USERNAME:$PASSWORD" | chpasswd
        log_audit "Created User: $USERNAME"
    else
        echo "[*] Updating password for existing user '$USERNAME'..."
        echo "$USERNAME:$PASSWORD" | chpasswd
        log_audit "Updated Password for User: $USERNAME"
    fi

    # Cleanup Conflicting Services
    echo "\n[*] Cleaning up conflicting RDP configurations..."
    if [[ "$RDP_SERVICE" == "gnome-remote-desktop" ]]; then
        systemctl stop xrdp 2>/dev/null || true
        backup_file "/etc/xrdp/xrdp.ini"
        apt-get purge -y xrdp xorgxrdp 2>/dev/null
        rm -rf /etc/xrdp
        track_package "xrdp xorgxrdp" "purged"
    elif [[ "$RDP_SERVICE" == "xrdp" ]]; then
        systemctl stop gnome-remote-desktop.service 2>/dev/null || true
        apt-get purge -y gnome-remote-desktop 2>/dev/null
        track_package "gnome-remote-desktop" "purged"
    fi

    # Clear Locks
    rm -f /tmp/.X*-lock
    rm -f /tmp/.X11-unix/X*
    log_audit "Cleared X11 Locks"

    # Extensive Repair
    if [[ "$REPAIR" == "true" ]]; then
        echo "\n[*] Initiating extensive system repair..."
        dpkg --configure -a
        apt-get update
        apt-get install -f -y
        apt-get autoremove -y
        apt-get install --reinstall -y dbus-x11 x11-xserver-utils
    fi

    # DE Installation
    echo "\n[*] Setting up Desktop Environment: $DESKTOP_MANAGER"
    apt-get update

    if [[ "$DESKTOP_MANAGER" == "Gnome" ]]; then
        if [[ "$REPAIR" == "true" ]]; then
            echo "    [!] Performing comprehensive GNOME reinstallation..."
            apt-get install --reinstall -y ubuntu-desktop ubuntu-desktop-minimal gnome-shell ubuntu-session gdm3 gnome-control-center
            
            USER_HOME=$(eval echo "~$USERNAME")
            if [[ -d "$USER_HOME" ]]; then
                echo "    [!] Resetting GNOME user configurations for $USERNAME..."
                backup_file "$USER_HOME/.config/dconf/user"
                rm -f "$USER_HOME/.config/dconf/user"
                
                for DIR in "gnome-session" "mutter" "monitors.xml"; do
                    if [[ -e "$USER_HOME/.config/$DIR" ]]; then
                        backup_file "$USER_HOME/.config/$DIR"
                        rm -rf "$USER_HOME/.config/$DIR"
                    fi
                done
                chown -R "$USERNAME:$USERNAME" "$USER_HOME/.config" 2>/dev/null || true
            fi
        else
            apt-get install -y ubuntu-desktop gdm3
        fi
        
        backup_file "/etc/X11/default-display-manager"
        echo "/usr/sbin/gdm3" > /etc/X11/default-display-manager
        dpkg-reconfigure gdm3 2>/dev/null || true
        systemctl enable gdm3.service
        
    elif [[ "$DESKTOP_MANAGER" == "XFCE4" ]]; then
        if [[ "$REPAIR" == "true" ]]; then
            apt-get install --reinstall -y xfce4 xfce4-goodies
            USER_HOME=$(eval echo "~$USERNAME")
            if [[ -d "$USER_HOME/.config/xfce4" ]]; then
                backup_file "$USER_HOME/.config/xfce4"
                rm -rf "$USER_HOME/.config/xfce4"
            fi
        else
            apt-get install -y xfce4 xfce4-goodies
        fi
    fi

    # TLS Setup
    echo "\n[*] Preparing TLS Certificates for RDP..."
    apt-get install -y ssl-cert openssl
    TLS_CERT="/etc/ssl/certs/rdp-custom-cert.pem"
    TLS_KEY="/etc/ssl/private/rdp-custom-key.pem"

    if [[ ! -f "$TLS_CERT" ]] || [[ ! -f "$TLS_KEY" ]]; then
        echo "    Generating new self-signed TLS certificate..."
        openssl req -x509 -nodes -days 3650 -newkey rsa:2048 \
            -keyout "$TLS_KEY" -out "$TLS_CERT" \
            -subj "/C=US/ST=State/L=City/O=Ubuntu/OU=RDP/CN=$(hostname)" &>/dev/null
        log_audit "Generated custom TLS certificates."
    fi
    chown root:ssl-cert "$TLS_KEY"
    chmod 640 "$TLS_KEY"
    chmod 644 "$TLS_CERT"

    # RDP Service Setup
    echo "\n[*] Configuring RDP Service: $RDP_SERVICE"
    if [[ "$RDP_SERVICE" == "gnome-remote-desktop" ]]; then
        apt-get install -y gnome-remote-desktop
        usermod -aG ssl-cert gnome-remote-desktop 2>/dev/null || true

        if [[ "$GNOME_MODE" == "System" ]]; then
            systemctl enable --now gnome-remote-desktop.service
            grdctl --system rdp set-tls-cert "$TLS_CERT"
            grdctl --system rdp set-tls-key "$TLS_KEY"
            grdctl --system rdp enable
            grdctl --system rdp set-credentials "$USERNAME" "$PASSWORD"
            systemctl restart gnome-remote-desktop.service
            log_audit "Configured GRD in System Mode."

        elif [[ "$GNOME_MODE" == "User" ]]; then
            usermod -aG ssl-cert "$USERNAME"
            GDM_CUSTOM_CONF="/etc/gdm3/custom.conf"
            if [[ -f "$GDM_CUSTOM_CONF" ]]; then
                backup_file "$GDM_CUSTOM_CONF"
                sed -i '/AutomaticLoginEnable/d' "$GDM_CUSTOM_CONF"
                sed -i '/AutomaticLogin/d' "$GDM_CUSTOM_CONF"
                sed -i '/\[daemon\]/a AutomaticLoginEnable=True\nAutomaticLogin='"$USERNAME"'' "$GDM_CUSTOM_CONF"
                log_audit "Injected AutoLogin into GDM3 custom.conf"
            fi
        fi

    elif [[ "$RDP_SERVICE" == "xrdp" ]]; then
        apt-get install -y xrdp xorgxrdp
        adduser xrdp ssl-cert

        backup_file "/etc/xrdp/xrdp.ini"
        sed -i "s|^certificate=.*|certificate=$TLS_CERT|g" /etc/xrdp/xrdp.ini
        sed -i "s|^key_file=.*|key_file=$TLS_KEY|g" /etc/xrdp/xrdp.ini

        USER_HOME=$(eval echo "~$USERNAME")
        backup_file "$USER_HOME/.xsession"
        if [[ "$DESKTOP_MANAGER" == "XFCE4" ]]; then
            echo "xfce4-session" > "$USER_HOME/.xsession"
        elif [[ "$DESKTOP_MANAGER" == "Gnome" ]]; then
            echo "gnome-session" > "$USER_HOME/.xsession"
        fi
        chown "$USERNAME:$USERNAME" "$USER_HOME/.xsession"

        XRDP_STARTWM="/etc/xrdp/startwm.sh"
        backup_file "$XRDP_STARTWM"
        if ! grep -q "export GNOME_SHELL_SESSION_MODE=ubuntu" "$XRDP_STARTWM"; then
            sed -i 's/^test -x \/etc\/X11\/Xsession/export GNOME_SHELL_SESSION_MODE=ubuntu\nexport XDG_CURRENT_DESKTOP=ubuntu:GNOME\n\ntest -x \/etc\/X11\/Xsession/g' "$XRDP_STARTWM"
        fi

        systemctl enable xrdp
        systemctl restart xrdp
        log_audit "Configured XRDP for $DESKTOP_MANAGER."
    fi

    # Firewall Setup
    if command -v ufw &> /dev/null; then
        ufw allow 3389/tcp >/dev/null
        ufw reload >/dev/null
        log_audit "Allowed Port 3389 in UFW."
    fi

    # Finalize Backup
    create_recovery_archive
    
    # Validations & Exit
    echo ""
    echo "==========================================================="
    echo " Setup Complete & Validations"
    echo "==========================================================="
    sleep 2
    if ss -tulpn | grep -q ":3389\b"; then
        echo -e "\e[32m[ PASS ] Port 3389 is actively listening.\e[0m"
    else
        echo -e "\e[33m[ INFO ] Port 3389 is not listening yet (expected if waiting for reboot/auto-login).\e[0m"
    fi
    
    if [[ "$REPAIR" == "true" ]]; then
         echo -e "\n\e[31m*** A REBOOT IS HIGHLY RECOMMENDED AFTER DEEP REPAIR ***\e[0m"
    fi
    
    read -p "Press Enter to return to main menu..."
}

# --- Main Menu ---

while true; do
    clear
    echo "================================================================="
    echo "  Ubuntu RDP Remediation & Configuration Toolkit v4 "
    echo "  Supports: 24.04, 25.10, 26.04                           "
    echo "================================================================="
    echo "  1) Run Comprehensive System Diagnostic Report"
    echo "  2) Install, Configure, or Repair RDP"
    echo "  3) Restore System from Backup Archive"
    echo "  4) Exit"
    echo "================================================================="
    read -p "Select an option [1-4]: " MENU_CHOICE

    case $MENU_CHOICE in
        1) run_system_report ;;
        2) run_install_repair ;;
        3) run_restore ;;
        4) exit 0 ;;
        *) echo "[!] Invalid choice." && sleep 1 ;;
    esac
done
